    private void BtnDepositoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDepositoActionPerformed
        // TODO add your handling code here:
        try {
            agregarLinea("Realizando Deposito");
            double monto = Double.parseDouble(JOptionPane.showInputDialog(this, "Ingrese Monto a depositar"));
            logeado.depositar(monto);
            agregarLinea("Deposito realizado por: "+monto);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Utilice solo numero y el punto como decimal", "Error", JOptionPane.ERROR_MESSAGE);
            agregarLinea("ERROR EN LA ENTRADA DE DATOS");
        } catch (Exception ex){
            agregarLinea("ERROR INSPERADO");
        }
    }//GEN-LAST:event_BtnDepositoActionPerformed

    private void BtnRetiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRetiroActionPerformed
        // TODO add your handling code here:
        try {
            agregarLinea("Realizando Retiro");
            double monto = Double.parseDouble(JOptionPane.showInputDialog(this, "Ingrese Monto a Retirar"));
            if (logeado.retirar(monto)) {
                agregarLinea("Fondos insuficuientes");
                return;
            }
            agregarLinea("Retiro realizado por: "+monto);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Utilice solo numero y el punto como decimal", "Error", JOptionPane.ERROR_MESSAGE);
            agregarLinea("ERROR EN LA ENTRADA DE DATOS");
        } catch (Exception ex){
            agregarLinea("ERROR INSPERADO");
        }
    }//GEN-LAST:event_BtnRetiroActionPerformed

